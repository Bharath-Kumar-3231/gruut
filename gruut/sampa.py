"""Mapping between IPA and Sampa"""
import unicodedata

# http://www.blahedo.org/ascii-ipa.html


def ipa_to_sampa(ipa: str) -> str:
    """Convert IPA string to sampa phonemes"""
    ipa_codepoints = unicodedata.normalize("NFD", ipa)
    sampa_codepoints = []
    for ipa_codepoint in ipa_codepoints:
        sampa_codepoint = IPA_TO_SAMPA.get(ipa_codepoint)
        if sampa_codepoint:
            sampa_codepoints.append(sampa_codepoint)

    return "".join(sampa_codepoints)


